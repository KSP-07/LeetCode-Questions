/*
  Reverse a linked list
  The input list will have at least one element  
  Return the node which points to the head of the new LinkedList
  Node is defined as 
    struct node
    {
        int data;
        struct node* next;
    
        node(int x){
            data = x;
            next = NULL;
        }
    
    }*head;
*/

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        // Complete this method
        if(!head || k<=1) return head;
        struct node *p= head , *q= head  , *r=NULL;
        int cnt = 0;
        
        while(p!=NULL && cnt<k){
            p=p->next;
            q->next = r;
            r=q;
            q=p;
            cnt++;
        }
        //after k reverse...4->2->2->1..head was originally on 1 so head->nxt means 1->next;
        if(p!=NULL) head->next = reverse(p , k);
        
        return r;
    }
};


